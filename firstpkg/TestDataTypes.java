package firstpkg;

public class TestDataTypes {

	public static void main(String[] args) {
		// Datatypes
		byte b=5;
		System.out.println("Value of Byte : "+b);
		
		short s=6;
		System.out.println("Value of Short : "+s);
		
		int i=100;
		System.out.println("Value of Int : "+i);
		
		long l=9946372590l;
		System.out.println("Value of Long : "+l);
		
		float f=1043.689f;
		System.out.println("Value of Float : "+f);
		
		double d=103.456;
		System.out.println("Value of Double : "+d);
		
		char c1='Q';
		System.out.println("Value of Char : "+c1);
		
		char c2=88;
		System.out.println("Value of Char(ASCII) : "+c2);
		
		boolean bl=12>24;
		System.out.println("Value of Boolen : "+bl);
		
		String st="Datatypes are working fine for me:)";
		System.out.println("Value of String : "+st);

	}

}
